def sumar(op1,op2):
	print ("la suma de los dos numeros es :" , op1+op2)

def restar(op1,op2):
	print("la resta de los dos numeros es : " , op1-op2)

def multiplicacion(op1,op2):
	print("la multiplicacion de los dos numeros es  : ", op1 * op2)

def division(op1,op2):
	print("la division de los dos numeros es : ", op1/op2)

def potencia(base,exponente):
	print("el resultado de la potencia es : ", base** exponente )

def redondear(numero):
	print("el numero redondeado es : ", round(numero))